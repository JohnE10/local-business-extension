<p><strong>5. Lawsuits and Legal Actions.</strong>&nbsp;We may disclose patient health information in response to (i) a court or administrative order or (ii) a subpoena, discovery request, or other lawful process that is not ordered by a court if efforts have been made to notify the patient or to obtain an order protecting the information requested.</p>



